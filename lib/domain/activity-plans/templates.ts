export function buildActivityPlanTemplates(directions: ActivityDirection[]): ActivityPlanTemplate[] {
  return [
    allDirectionsTemplate,
    ...directions.map((direction) => {
      const override = directionTemplateOverrides[direction.id];

      return {
        id: `template-${direction.id}`,
        directionId: direction.id,
        title: override?.title ?? `План деятельности по направлению «${direction.title}»`,
        shortTitle: override?.shortTitle ?? direction.title,
        goal: override?.goal ?? `Организация системной работы школы по направлению «${direction.title}».`,
        tasks: override?.tasks ?? [
          "сформировать перечень мероприятий по выбранному направлению;",
          "обеспечить распределение сроков, классов и ответственных;",
          "использовать план как проекцию единого реестра мероприятий без дублирования данных."
        ],
        defaultGrouping: override?.defaultGrouping ?? "month"
      };
    })
  ];
}

export function findActivityPlanTemplate(
  templates: ActivityPlanTemplate[],
  directionId: string | "all"
) {
  return templates.find((template) => template.directionId === directionId) ?? allDirectionsTemplate;
}
